// SYNTAX FOR DECLARING VARIABLES IN C++:

// [DATA_TYPE VARIABLE_NAME=VALUE;]
// EX.> [int a=4, b=6;]

// THERE ARE 3 DATA TYPES:

// 1>BUILT IN-> [int, float, char, double, boolean(bool)]
// 2>USER DEFINED-> [struct, union, enum]
// 3>DERIVED-> [array, function, pointer]

// built in examples:

// 1>int-> 1,2,6,9,7 [integers]
// 2>float->3.14, 5.09 [small decimal]
// 3>char->a,b,h,k,g [alphabets or letter]
// 4>double->3.12454, 5.07448 [large decimal]
// 5>boolean-> true or false [t=1, f=0]

